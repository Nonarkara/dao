# Build a simple tokeniser to find play arrays and inject body_th / title_th
# Strategy: walk the file character by character, track braces, find play arrays

def inject_th_fields(text):
    """Find each play array and inject body_th/title_th into cards that lack them."""
    # We work by finding  play: [  then walking cards sequentially
    result = []
    pos = 0
    chapter_num = None
    card_counter = {}  # chapter -> card index

    while pos < len(text):
        # Track current chapter number
        m_ch = re.search(r'^\s*(\d+):\s*\{', text[pos:pos+20])
        if m_ch:
            chapter_num = int(m_ch.group(1))

        # Look for play: [
        if text[pos:pos+7] == 'play: [':
            # Find matching ]
            start = pos + 7
            depth = 1
            i = start
            while i < len(text) and depth > 0:
                if text[i] == '[': depth += 1
                elif text[i] == ']': depth -= 1
                i += 1
            play_block = text[start:i-1]

            # Patch each card object in the play block
            patched = patch_play_block(play_block, chapter_num, card_counter)
            result.append(text[pos:pos+7])
            result.append(patched)
            result.append(']')
            pos = i
            continue

        result.append(text[pos])
        pos += 1

    return ''.join(result)


def patch_play_block(block, chapter_num, card_counter):
    """Insert body_th/title_th into each {…} card in the play block."""
    if chapter_num not in card_counter:
        card_counter[chapter_num] = 0

    result = []
    pos = 0
    while pos < len(block):
        if block[pos] == '{':
            # Find matching }
            depth = 1
            i = pos + 1
            while i < len(block) and depth > 0:
                if block[i] == '{': depth += 1
                elif block[i] == '}': depth -= 1
                i += 1
            card = block[pos:i]
            card_idx = card_counter.get(chapter_num, 0)
            card_counter[chapter_num] = card_idx + 1

            key = (chapter_num, card_idx)
            th = TH_CONTENT.get(key)

            if th and 'body_th' not in card:
                title_th, body_th = th
                # Insert before closing }
                insert = ''
                if 'title_th' not in card:
                    insert += f',\n        title_th: "{title_th}"'
                insert += f',\n        body_th: "{body_th}"'
                card = card[:-1] + insert + '}'

            result.append(card)
            pos = i
        else:
            result.append(block[pos])
            pos += 1

    return ''.join(result)

